#include <stdio.h>
int main() {
  int x = 10; // x ਨੂੰ 10 ਦਾ ਮੁੱਲ ਦਿਓ
  {
    int y = 20; // y ਨੂੰ 20 ਦਾ ਮੁੱਲ ਦਿਓ (ਇਹ ਸਿਰਫ਼ ਇਸ ਬਲਾਕ ਵਿੱਚ ਉਪਲਬਧ ਹੈ)
    printf("x = %d, y = %d\n", x, y); // x ਅਤੇ y ਦੇ ਮੁੱਲ ਪ੍ਰਿੰਟ ਕਰੋ
  }

  // y ਇੱਥੇ ਉਪਲਬਧ ਨਹੀਂ ਹੈ ਕਿਉਂਕਿ ਇਹ ਸਿਰਫ਼ ਉੱਪਰਲੇ ਬਲਾਕ ਵਿੱਚ ਮੌਜੂਦ ਹੈ
  printf("x = %d\n", x); // ਸਿਰਫ਼ x ਦਾ ਮੁੱਲ ਪ੍ਰਿੰਟ ਕਰੋ

  return 0;
}